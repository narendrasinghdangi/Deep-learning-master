SOS_token = 0
EOS_token = 1


class Lang:
    def __init__(self, name):
        self.name = name
        self.wtoi = {}
        self.wtoc = {}
        self.itow = {0: "SOS", 1: "EOS"}
        self.n_words = 2  # Count SOS and EOS

    def addSentence(self, sentence):
        for word in sentence.split(' '):
            self.addWord(word)

    def addWord(self, word):
        if word not in self.wtoi:
            self.wtoi[word] = self.n_words
            self.wtoc[word] = 1
            self.itow[self.n_words] = word
            self.n_words += 1
        else:
            self.wtoc[word] += 1


